def generate_user_activity_pdf(filename, admin_name, users):
    """
    Generate PDF report for user activity
    
    Args:
        filename: Output PDF filename
        admin_name: Name of administrator generating the report
        users: List of user dictionaries
    """
    generator = PDFReportGenerator(filename, "User Activity Report")
    
    # Add title
    generator.add_title()
    
    # Add header info
    generator.add_header_info(admin_name, "User Activity Report")
    
    # Calculate role distribution
    role_counts = {}
    active_counts = {}
    
    for user in users:
        role = user.get('role', 'unknown')
        role_counts[role] = role_counts.get(role, 0) + 1
        if user.get('is_active', False):
            active_counts[role] = active_counts.get(role, 0) + 1
    
    # Role Distribution Table
    role_data = []
    for role in ['admin', 'doctor', 'nurse', 'patient']:
        total = role_counts.get(role, 0)
        active = active_counts.get(role, 0)
        inactive = total - active
        role_data.append([
            role.title(),
            str(total),
            str(active),
            str(inactive)
        ])
    
    generator.add_table(
        role_data,
        ["Role", "Total Users", "Active", "Inactive"],
        "User Distribution by Role"
    )
    
    # User Details Table (sample of first 20 users)
    if users:
        user_headers = ["Username", "Full Name", "Email", "Role", "Status"]
        user_data = []
        for user in users[:20]:  # Limit to first 20 for readability
            user_data.append([
                user.get('username', 'N/A'),
                user.get('full_name', 'N/A'),
                user.get('email', 'N/A'),
                user.get('role', 'N/A').title(),
                "Active" if user.get('is_active', False) else "Inactive"
            ])
        
        generator.add_table(user_data, user_headers, "User Details (Sample)")
        
        if len(users) > 20:
            generator.add_paragraph(
                f"Note: Showing first 20 users out of {len(users)} total users.",
                generator.normal_style
            )
    
    # Summary
    total_users = len(users)
    total_active = sum(1 for u in users if u.get('is_active', False))
    summary = {
        "Total Users": total_users,
        "Active Users": total_active,
        "Inactive Users": total_users - total_active,
        "Report Generated": datetime.now().strftime('%Y-%m-%d %H:%M:%S')
    }
    generator.add_summary_section(summary)
    
    # Build PDF
    return generator.build()


